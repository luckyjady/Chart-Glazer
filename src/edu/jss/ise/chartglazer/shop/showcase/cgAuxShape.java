package edu.jss.ise.chartglazer.shop.showcase;

import java.awt.Point;

/**
 * Contains the common features for all the auxiliary shapes (supporting shapes which do not have text content.)
 * @author rishabh.
 */
abstract public class cgAuxShape extends cgShape
{	
	/**
	 * Creates empty implementaions for data members.
	 */
	public cgAuxShape()
	{
		super();
	}

	/**
	 * Constructs this auxiliary shape object based on the given <code>shapeCenter</code>.
	 * @param shapeCenter The center coordinates of this shape.
	 * @throws IllegalArgumentException
	 */
	public cgAuxShape(Point shapeCenter) throws IllegalArgumentException
	{
		super(shapeCenter);
	}

	@Override
	public String toString()
	{
		return super.toString();
	}
}
